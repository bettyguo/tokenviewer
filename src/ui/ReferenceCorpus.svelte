            </th>
            {#each corpus as c (c.id)}
              <td class:lower={metric === 'tokenCount'} style:--h={heat(t.code, c.id)}>
                {show(t.code, c.id)}
              </td>
            {/each}
          </tr>
        {/each}
      </tbody>
    </table>
  </div>

  <ul class="findings">
