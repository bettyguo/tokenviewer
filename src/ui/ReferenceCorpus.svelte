      </button>
    </div>
  </div>

  <div class="scroll-x">
    <table class="mono">
      <thead>
        <tr>
          <th class="corner">tokenizer</th>
          {#each corpus as c (c.id)}
            <th title={c.note}>{c.id}</th>
          {/each}
        </tr>
      </thead>
      <tbody>
        {#each tokenizers as t (t.code)}
          <tr>
            <th class="rowh">
